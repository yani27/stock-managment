            prod.append("numero_produit", Integer.valueOf(numero_prod));
            prod.append("prix", Float.valueOf(prix));
            prod.append("marque", marque);
            prod.append("modele", modele);
            prod.append("vendu", false);
            prod.append("id_fournisseur", fornisseur);
            
            if(!(activityExists(Main.db,quantite)))
            prod.append("id_stock", stock);
            
                  
                
                
                 Main.db.getCollection("stock").insertOne(stock);
