        String chosen_fournisseur = String.valueOf(jcbf.getSelectedItem());   
        String nom_prod = txtnom.getText();
        String quantite = txtquantite.getText();
        String numero_prod = txtnumero.getText();
        String prix = txtprix.getText();
        String marque = txtmarque.getText();
        String modele = txtmodel.getText();
        System.out.println(chosen_fournisseur);
        org.bson.Document fornisseur = Main.db.getCollection("fournisseur").find(new BasicDBObject("nom_prenom_fournisseur", chosen_fournisseur)).first();
